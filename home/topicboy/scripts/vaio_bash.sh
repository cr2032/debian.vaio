#!/bin/bash
CYN='\033[0;36m' # Cyan
BLU='\033[0;34m' # Blue
PUR='\033[0;35m' # Purple
RED='\033[0;31m' # Red
IRE='\033[0;91m' # Red
YLW='\033[0;33m' # Yellow
IYL='\033[0;93m' # Yellow

echo "${CYN}#   .:                .:.          ,;.       ...             =++===-    "
