echo "${RED}#      .%@:      +@*  .:  :@%.     *@:  .@%      :@*       +   -..::    "
echo "${IRE}#        *@+   .#@=   @@+  .%@-    *@:   \#@=    .#@:  @    :+          "
echo "${YLW}#         -%@##@#.     .     =%@=  *@:    =%@#::%@*           .-:       "
echo ""
